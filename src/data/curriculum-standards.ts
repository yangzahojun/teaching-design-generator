// 按学科检索课标
export function searchStandards(subject: string): StandardEntry[] {
  if (!subject) return CURRICULUM_STANDARDS;
  return CURRICULUM_STANDARDS.filter(s => s.subject === subject);
}

// 按学科检索教材链接
export function searchTextbooks(subject: string): TextbookLink[] {
  if (!subject || subject === '全部学科') return TEXTBOOK_LINKS;
  return TEXTBOOK_LINKS.filter(t =>
    t.subject === subject || t.subject.startsWith(subject) || t.subject === '全部学科'
  );
}
